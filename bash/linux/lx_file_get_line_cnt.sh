################################################################################
#  VARIABLES                                                                   #
################################################################################
LX_FILE_PATH="$1"

################################################################################
#  FUNCTIONS                                                                   #
################################################################################

check_dependencies()
{
  # check xdotool is installed
  if ! command -v wc &> /dev/null
  then
    echo "wc could not be found"
    exit
  fi
}

lx_file_get_line_cnt()
{
  wc -l <$LX_FILE_PATH
}

################################################################################
#  APPLICATION                                                                 #
################################################################################

check_dependencies
lx_file_get_line_cnt

